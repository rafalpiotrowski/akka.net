using System;
using System.Collections.Generic;
using System.Linq;

namespace Akka.Util
{
    /// <summary>
    /// Utility class for creating vectors (collections) of elements.
    /// </summary>
    public static class Vector
    {
        /// <summary>
        /// Creates a function that fills a collection with a specified number of elements 
        /// generated by the provided factory function.
        /// </summary>
        /// <typeparam name="T">The type of elements to create.</typeparam>
        /// <param name="number">The number of elements to create.</param>
        /// <returns>A function that takes an element factory and returns a list of generated elements.</returns>
        public static Func<Func<T>, IList<T>> Fill<T>(int number)
        {
            return func => Enumerable
                .Range(1, number)
                .Select(_ => func())
                .ToList();
        } 
    }
}
